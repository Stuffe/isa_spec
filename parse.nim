    skip(s)
    result[1].finish += 1
  add_token(s, tk_number)
  return result

func xdigit_to_value*(c: char): int =
  # Assumes that c has already been verified
  if c in DIGITS:
    return c.ord - '0'.ord
  if c in {'A' .. 'Z'}:
    return c.ord - 'A'.ord + 10
  return c.ord - 'a'.ord + 10

func parse_unsigned*(s: StreamSlice): (string, uint64) =
  var start_digits = 0
  if s[0] == '-':
    return (translate(31337_51855228989260, "Invalid unsigned int literal"), 0'u64)

  if s[0] == '+':
    start_digits = 1

  var base = 0'u64
  var digits = {'_'}

  if s.len - start_digits < 3 or s[start_digits + 1] notin {'x', 'o', 'b'}:
    base = 10
    digits = digits + {'0' .. '9'}
  else:
    case s[start_digits + 1]
    of 'x':
      base = 16
      digits = digits + HexDigits
    of 'o':
      base = 8
      digits = digits + {'0' .. '7'}
    else:
      base = 2
      digits = digits + {'0' .. '1'}

    start_digits += 2

  let overflow_threshold_mul = uint64.high div base
  var num = 0'u64
  for i in start_digits ..< s.len:
    if s[i] notin digits:
      return (translate(31337_64926004981217, "Invalid int literal"), 0'u64)

    if s[i] == '_':
      continue

    if num > overflow_threshold_mul:
      return (translate(31337_33551107641907, "Int literal too big"), 0'u64)

    num *= base

    let digit = cast[uint64](xdigit_to_value(s[i]))
    if num > uint64.high - digit:
      return (translate(31337_33551107641907, "Int literal too big"), 0'u64)
    num += digit

  return ("", num)

func parse_signed*(s: StreamSlice): (string, uint64) =
  var is_negative = false
  var start_digits = 0
  if s[0] == '-':
    is_negative = true
    start_digits = 1
  elif s[0] == '+':
    start_digits = 1

  var base = 0'u64
  var digits = {'_'}

  if s.len - start_digits < 3 or s[start_digits + 1] notin {'x', 'o', 'b'}:
    base = 10
    digits = digits + {'0' .. '9'}
  else:
    case s[start_digits + 1]
    of 'x':
      base = 16
      digits = digits + HexDigits
    of 'o':
      base = 8
      digits = digits + {'0' .. '7'}
    else:
      base = 2
      digits = digits + {'0' .. '1'}

    start_digits += 2

  let overflow_threshold_mul = uint64.high div base
  var num = 0'u64
  for i in start_digits ..< s.len:
    if s[i] notin digits:
      return (translate(31337_64926004981217, "Invalid int literal"), 0'u64)

    if s[i] == '_':
      continue

    if num > overflow_threshold_mul:
      return (translate(31337_33551107641907, "Int literal too big"), 0'u64)

    num *= base

    let digit = cast[uint64](xdigit_to_value(s[i]))
    if num > uint64.high - digit:
      return (translate(31337_33551107641907, "Int literal too big"), 0'u64)
    num += digit

  if is_negative:
    if num > int64.high.uint64 + 1:
      return (translate(31337_33551107641907, "Int literal too big"), 0'u64)

    num = (not num) + 1
  else:
    if num > int64.high.uint64:
      return (translate(31337_33551107641907, "Int literal too big"), 0'u64)

  return ("", num)

