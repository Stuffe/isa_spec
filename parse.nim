func get_enum*[T](
    s: var StreamSlice, options: openArray[(string, T)], tk = tk_identifier
): (string, T) =
  for (str, value) in options:
    if s.matches(str, tk = tk):
      return ("", value)

  var joined_options = ""
  for i, (str, _) in options:
    if i != 0: # Not the first element
      joined_options &= ", "
    joined_options &= str
  let current = get_identifier(s, tk = tk)
  if current.len != 0:
    return (
      translate(31337_19807940646400, "Expected one of {expected}, got {actual}")
      .multiReplace(("expected", joined_options), ("actual", $current)),
      default(T),
    )
  else:
    return (
      translate(31337_73113688099439, "Expected one of {expected}").replace(
        "expected", joined_options
      ),
      default(T),
    )

