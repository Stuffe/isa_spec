func get_size*(s: var StreamSlice): (string, uint64, bool) =
  let cp = checkpoint(s)

  if peek(s) notin {'U', 'S'}:
    result[0] =
      translate(31337_43326454052643, "Expected an U for unsigned or S for signed here")
    return

  result[2] = read(s) == 'S'

  let s_number = get_unsigned(s).on_err:
    result[0] = err
    return

  change_token_kind(s, tk_number, tk_type_name)

  if s_number.len == 0:
    s.restore(cp)
    result[0] = translate(31337_77089554485217, "Expected a size declaration here")
    return
  add_token(s, tk_type_name)

  let number = parse_unsigned(s_number).on_err:
    result[0] = err
    return

  result[1] = number

