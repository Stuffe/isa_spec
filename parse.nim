func get_hex*(s: var StreamSlice, prefix: string = "0x"): (string, StreamSlice) =
  result[1] = s
  result[1].finish = s.start

  if not matches(s, prefix, increment = false):
    return (
      translate(31337_72110029133381, "Expected {prefix}", ("prefix", prefix)),
