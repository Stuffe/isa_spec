func checkpoint*(s: StreamSlice): RestorePoint =
  result.s = s

  if isNil(s.tokens):
    return

  result.token_count = s.tokens[].len
  result.last_token_finish =
    if s.tokens[].len > 0:
      s.tokens[][^1].finish
    else:
      0

func restore*(s: var StreamSlice, cp: RestorePoint) =
  assert s.source == cp.s.source, "RestorePoint does not apply to this stream"
  s = cp.s

  if isNil(s.tokens):
    return

  assert cp.token_count <= s.tokens[].len, "`restore` cannot recreate deleted tokens"
  s.tokens[].set_len(cp.token_count)
  if cp.token_count > 0:
    s.tokens[][^1].finish = cp.last_token_finish

