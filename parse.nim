func finished*(s: StreamSlice): bool =
  assert not isNil(s.source)
  return s.start >= s.finish

func len*(s: StreamSlice): int =
  assert not isNil(s.source)
  return s.finish - s.start

func `$`*(s: StreamSlice): string =
