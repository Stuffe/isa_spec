func get_string*(
    s: var StreamSlice, tk = tk_string, tk_quote = tk
): (string, StreamSlice) =
  let cp = s.checkpoint()

  let quote = s.read(tk = tk_quote)

  if quote notin QUOTES:
    s.restore(cp)
    return (
      translate(31337_27682737127628, "Expected an opening quote (one of \", ' or `)"),
      empty_slice(s),
    )

  while peek(s) != quote:
    if read(s) == '\\':
      discard read(s)
    if finished(s):
      s.restore(cp)
      return (
        translate(31337_75565287060042, "Unexpected EOF while parsing a string"),
        empty_slice(s),
      )

  add_token(s, tk)

  discard read(s, tk = tk_quote)
  result[1] = s
  result[1].start = cp.s.start + 1
  result[1].finish = s.start - 1

func descape_string_content*(s: StreamSlice): (string, string) =
  var it = s
  while not it.finished():
    let c = it.read()
    if c != '\\':
      result[1].add c
      continue
    if it.finished():
      return (translate(31337_10198498286711, "Unfinished escape sequence"), "")
    let nc = it.read()
    case nc
    of 'a':
      result[1].add '\a'
    # Bell (Alert)
    of 'b':
      result[1].add '\b'
    # Backspace
    of 'f':
      result[1].add '\f'
    # Formfeed
    of 'n':
      result[1].add '\n'
    # Linefeed (newline)
    of 'r':
      result[1].add '\r'
    # Carriage Return
    of 't':
      result[1].add '\t'
    # Horizontal Tab
    of 'v':
      result[1].add '\v'
    # Vertical Tab
    of '\\':
      result[1].add '\\'
    of '"':
      result[1].add '"'
    of '\'':
      result[1].add '\''
    of '`':
      result[1].add '`'
    of '0' .. '9':
      var value = nc.ord - '0'.ord
      if peek(it) in DIGITS:
        value = 8 * value + read(it).ord - '0'.ord
      if peek(it) in DIGITS:
        value = 8 * value + read(it).ord - '0'.ord
      if value > 255:
        return (translate(31337_36313699368590, "Invalid octal escape sequence"), "")
      result[1].add char(value)
    of 'x':
      var digit1 = read(it)
      var digit2 = read(it)
      if digit1 not_in HEX_DIGITS or digit2 not_in HEX_DIGITS:
        return (translate(31337_44942745729831, "Invalid hex escape sequence"), "")
      let value = xdigit_to_value(digit1) * 16 + xdigit_to_value(digit2)
      result[1].add char(value)
    else:
      return (
        translate(31337_56457570130031, "Invalid escape character '{nc}'", ("nc", nc)),
        "",
      )

func make_escaped_string*(s: string, quote = '"'): string =
  result &= quote
  for c in s:
    if c in {quote, '\\'}:
      result &= "\\" & c
    if c in {'\x00' .. '\x19'}:
      result &= "\\x" & toHex(c.ord, 2)
    else:
      result &= c
  result &= quote

func parse_string*(s: StreamSlice): (string, string) =
  # To be used with results from get_list_value
  var it = s
  let content = it.get_string().on_err:
    return (err, "")
  return descape_string_content(content)

iterator get_encapsulation*(s: var StreamSlice, tk = tk_bracket): StreamSlice =
  assert not isNil(s.source)

  let cp = s.checkpoint()

  let open = read(s, tk)
  var close: char

  case open
  of '(':
    close = ')'
  of '[':
    close = ']'
  of '{':
    close = '}'
  else:
    s.restore(cp)
      # TODO: Ideally we would not restore here (and in other places where we raise) I think, but that's a behavior change for later
    raise newParseError(
      s,
      translate(
        31337_33400766069212,
        "Expected an opening parantheses (one of '(', '[' or '{{'), got '{open}'",
        ("open", open),
      ),
    )

  skip_newlines(s)
  var inner = s

  var depth = 1
  while true:
    let c = read(s)
    if c == open:
      depth += 1
    elif c == close:
      depth -= 1
      if depth == 0:
        break
    elif c in QUOTES:
      s.start -= 1
      discard get_string(s, tk_none).on_err:
        s.restore(cp)
        raise newParseError(s, err)
    elif finished(s):
      s.restore(cp)
      raise newParseError(
        s,
        translate(
          31337_25983481845150, "Expected '{symbol}', got EOF", ("symbol", close)
        ),
      )

  inner.finish = s.start - 1
  yield inner
  add_token(s, tk)

proc skip_encapsulation*(s: var StreamSlice) =
  for _ in get_encapsulation(s, tk_none):
    discard

func strip*(
    s: StreamSlice
): StreamSlice {.
    deprecated:
      "For better token production, manually use skip_whitespaces/skip_newlines"
.} =
  result = s
  while peek(result) in WHITESPACES + {'\n'}:
    skip(result)
  while result.finish - 1 > 0 and
      result.source[][result.finish - 1] in WHITESPACES + {'\n'}:
    result.finish -= 1

func from_line_start*(s: StreamSlice): StreamSlice =
  result = s
  # Go back one if we are at a newline
  if result.start > 0 and peek(result) == '\n':
    result.start -= 1

  # Seek backwards until the start of the line
  while result.start > 0 and peek(result) != '\n':
    result.start -= 1

  # Go forwards from the start of that line to the end
  result.start += 1
  result.finish = result.start
  while result.finish < s.len and result.source[result.finish] != '\n':
    result.finish += 1

func from_line_start_to_here*(s: StreamSlice): StreamSlice =
  result = s
  result.finish = s.start

  # Go back one if we are at a newline
  if result.start > 0 and peek(result) == '\n':
    result.start -= 1

  # Seek backwards until the start of the line
  while result.start > 0 and peek(result) != '\n':
    result.start -= 1

  if peek(result) == '\n':
    result.start += 1

func get_list_value(s: var StreamSlice): (string, StreamSlice) =
  # This function is not allowed to call add_token with tk!=tk_none
  assert not isNil(s.source)

  let start = s.start

  case peek(s)
  of QUOTES: # These may contain commas
    discard get_string(s, tk = tk_none).on_err:
      return (err, empty_slice(s))
  of '(', '[', '{': # These may contain commas
    skip_encapsulation(s)
  else:
    while peek(s) notin WHITESPACES + {',', ':', '\n', '\0'}:
      skip(s)

  result[1] = s
  result[1].start = start
  result[1].finish = s.start
  result[1] = result[1]

iterator get_list*(s: var StreamSlice): StreamSlice =
  let restore = s
  for raw_inner in get_encapsulation(s):
    var list = raw_inner
    while not finished(list):
      skip_newlines(list)

      let start = list.start
      let element = get_list_value(list).on_err:
        s = restore
        raise newParseError(s, err)
      if list.start == start:
        s = restore
        raise
          newParseError(s, translate(31337_64743950507593, "Expecated a list value"))

      yield element

      skip_newlines(list)

      if peek(list) == ',':
        skip(list, tk = tk_separator)
        skip_newlines(list)

proc get_whole_list*(s: var StreamSlice): seq[StreamSlice] =
  for value in get_list(s):
    result.add(value)

iterator get_table*(s: var StreamSlice): (bool, StreamSlice) =
  # yields (is_value, text). Will always generate a key followed by a value unless an exception is raised

  let restore = s
  for raw_inner in get_encapsulation(s):
    var list = raw_inner
    while not finished(list):
      skip_whitespaces(list)

      let key = get_list_value(list).on_err:
        s = restore
        raise newParseError(s, err)
      if key.len == 0:
        s = restore
        raise newParseError(s, translate(31337_13040813748160, "Expected a key"))
          # Of a table (map) data structure
      yield (false, key)
      skip_whitespaces(list)

      if read(list, tk_separator) != ':':
        s = restore
        raise newParseError(
          s, translate(31337_66187706284192, "Expected ':' after the key")
        ) # Of a table (map) data structure
      skip_whitespaces(list)

      let value = get_list_value(list).on_err:
        s = restore
        raise newParseError(s, err)

      if value.len == 0:
        s = restore
        raise newParseError(s, translate(31337_49180229055343, "Expected a value"))
          # Of a table (map) data structure

      yield (true, value)

      skip_whitespaces(list)
      if peek(list) == ',':
        skip(list, tk = tk_separator)
        skip_newlines(list)
      elif not finished(list):
        s = restore
        raise newParseError(
          s, translate(31337_12492443144951, "Expected a ',' or end of table")
        ) # Of a table (map) data structure

proc get_whole_table*(s: var StreamSlice): OrderedTable[StreamSlice, StreamSlice] =
  var key: StreamSlice

  for is_value, value in get_table(s):
    if not is_value:
      key = value
    else:
      result[key] = value

func parse_parametrized_pattern*(
    s: var StreamSlice, parameters: seq[StreamSlice]
): (seq[string], seq[int]) =
  var slice = s
  slice.finish = slice.start

  while peek(s) notin {'\n', '\0'} or peek(s, 1) notin {'[', '@'}:
    if peek(s) != '`':
      skip(s, tk = tk_text)
      slice.finish = s.start
      continue

    let (err, parameter_name) = get_string(s, tk = tk_pattern_variable)
    if err != "":
      skip(s, tk = tk_text)
      slice.finish = s.start
      continue

    if parameter_name.len <= 0:
      slice.finish = s.start
      continue

    let parameter_index = parameters.find(parameter_name)
    if parameter_index < 0:
      slice.finish = s.start
      change_token_kind(s, tk_pattern_variable, tk_text)
      continue

    result[0].add($slice)
    result[1].add(parameter_index)

    slice.start = s.start
    slice.finish = s.start

  slice.finish += 1
  result[0].add($slice)

  discard read(s, tk = tk_whitespace)

func get_table_value*(s: var StreamSlice): string =
  assert not isNil(s.source)

  skip_whitespaces(s)

  var closing_char: char
  var c = read(s)
  if c in QUOTES:
    closing_char = c
  else:
    raise newParseError(
      s,
      translate(
        31337_11064758781325,
        "Expected '\"', ''', or '`' at the start of UI table cell content",
      ),
    )

  var was_escape = false
  while true:
    c = peek(s)
    if c in {'\r', '\n', '\0'}:
      if was_escape:
        raise newParseError(
          s,
          translate(
            31337_82386938622647,
            "Expected some character after the escape character '\\'",
          ),
        )
      break
    skip(s)

    if was_escape:
      result &= c
      was_escape = false
    elif c == closing_char:
      break
    elif c == '\\':
      was_escape = true
    else:
      result &= c

  skip_whitespaces(s)

iterator get_ui_table*(
    s: var StreamSlice
): tuple[is_row_header: bool, content: string] =
  if read(s, tk_bracket) != ']':
    raise newParseError(
      s, translate(31337_85095060656242, "Expected ']' at the start of the UI table")
    )

  var is_row_header = true

  skip_newlines(s)
  while peek(s) != '[':
    skip_whitespaces(s)
    let (error0, raw_cell) = get_string(s)
    if error0 != "":
      raise newParseError(s, error0)

    let (error1, content) = descape_string_content(raw_cell)
    if error1 != "":
      raise newParseError(s, error1)

    yield (is_row_header, content)
    is_row_header = false

    skip_whitespaces(s)

    let next_char = peek(s)
    if next_char == '\0':
      raise newParseError(
        s, translate(31337_33545106210094, "Expected '[' at the end of the UI table")
      )
    if next_char in {'\n', '\r'}:
      is_row_header = true
      skip_newlines(s)
    elif next_char == ',':
      skip(s, tk = tk_separator)
    else:
      raise newParseError(
        s,
        translate(
          31337_66543091234026,
          "Expected ',' or '\\n' at the end of the UI table cell content",
        ),
      )

  if finished(s):
    raise newParseError(
      s, translate(31337_49158020311750, "Expected '[' at the end of the UI table")
    )

  skip(s, tk = tk_bracket)

