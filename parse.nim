func get_number(
    s: var StreamSlice, base: var int, optional_start_symbols: set[char]
): (string, StreamSlice) =
  result[1] = s
  result[1].finish = s.start

  if peek(s) in optional_start_symbols:
    result[1].finish += 1
    skip(s)

  if peek(s) in {'0', '#'}:
    if peek(s, 1) == 'x':
      const HEX = HexDigits + {'_'}

      skip(s, 2)
      result[1].finish += 2
      while peek(s) in HEX:
        skip(s)
        result[1].finish += 1
      add_token(s, tk_number)
      base = 16
      return result
    if peek(s, 1) == 'o':
      const OCT = Digits - {'8', '9'} + {'_'}

      skip(s, 2)
      result[1].finish += 2
      while peek(s) in OCT:
        skip(s)
        result[1].finish += 1
      add_token(s, tk_number)
      base = 8
      return result
    if peek(s, 1) == 'b':
      const BIN = {'0', '1', '_'}

      skip(s, 2)
      result[1].finish += 2
      while peek(s) in BIN:
        skip(s)
        result[1].finish += 1
      add_token(s, tk_number)
      base = 2
      return result

  const DEC_FIRST = Digits
  const DEC_NEXT = DEC_FIRST + {'_'}

  if peek(s) notin DEC_FIRST:
    return
      (translate(31337_22452670489981, "Expected a number literal"), empty_slice(s))
  skip(s)
  result[1].finish += 1
  while peek(s) in DEC_NEXT:
    skip(s)
    result[1].finish += 1
  add_token(s, tk_number)
  base = 10

