func add_token*(s: StreamSlice, tk: TokenKind) =
  # Adds a token starting from the end of the last token to the start of s
  if tk == tk_none or isNil(s.tokens):
    return

  let start =
    if s.tokens[].len == 0:
      0
    else:
      s.tokens[][^1].finish

  let finish = s.start.min(s.source[].len)

  if tk in CONTROL_TOKENS:
    assert start == finish
    s.tokens[].add(Token(start: start, finish: finish, tk: tk))
    return

  if start == finish: # Token would be zero length, don't generate it at all
    return
  assert finish > start,
    &"Invalid call to add_token, forgot to restore properly? (start={start}, finish={finish})"
  if s.tokens[].len > 0 and s.tokens[][^1].tk == tk:
    s.tokens[][^1].finish = finish
  else:
    s.tokens[].add(Token(start: start, finish: finish, tk: tk))

func change_token_kind*(s: StreamSlice, expected: TokenKind, new_token: TokenKind) =
  if isNil(s.tokens):
    return

  assert s.tokens[][^1].tk == expected
  s.tokens[][^1].tk = new_token

func peek*(s: StreamSlice): char =
  assert not isNil(s.source)
  if s.start >= s.finish:
    return
  return s.source[s.start]

func peek*(s: StreamSlice, offset: int): char =
  assert not isNil(s.source)
  let i = s.start + offset
  if i >= s.finish:
    return
  return s.source[i]

func skip*(s: var StreamSlice, amount = 1, tk = tk_none) =
  assert not isNil(s.source)
  s.start += amount
  if s.start > s.finish:
    s.start = s.finish
  add_token(s, tk)

func read*(s: var StreamSlice, tk = tk_none): char =
  assert not isNil(s.source)
  if s.start > s.source[].high:
    return
  result = s.source[s.start]
  s.start += 1
  add_token(s, tk)

func take*(s: var StreamSlice, c: char): bool =
  if s.start > s.source[].high:
    return
  result = s.source[s.start] == c
  if result:
    s.start += 1

func matches*(s: var StreamSlice, value: string, increment = true, tk = tk_none): bool =
  for i in 0 .. value.high:
    if peek(s, i) != value[i]:
      return false
  if increment:
    s.start += value.len
    add_token(s, tk)
  return true

